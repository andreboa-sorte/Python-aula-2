#16 - Faça um algoritmo que leia os valores de COMPRIMENTO, LARGURA e
# ALTURA e apresente o valor do volume de uma caixa retangular.
# Utilize para o cálculo a fórmula VOLUME = COMPRIMENTO * LARGURA * ALTURA.

comprimento=int(input("digite o comprimento da caixa: "))
largura=int(input("digite a largura da caixa: "))
altura=int(input("digite a altura da caixa: "))

volume=comprimento*largura*altura

print("o volume da caixa é: ", volume)
